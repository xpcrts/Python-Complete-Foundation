# Initiate new variables
# This is also an assignment operation
num1 = 202

# Plus-equal of compound assignment operator
num1 += 2
print(num1)

# minus-equal of compound assignment operator
num1 -= 2
print(num1)

# multiplication-equal of compound assignment operator
num1 *= 2
print(num1)

# division-equal of compound assignment operator
num1 /= 2
print(num1)

# modulo-equal of compound assignment operator
num1 %= 2
print(num1)