# Initiate new variables
a = 100
b = -100

# Ternary operation 
print("a is greater than b") if a > b else print("a is less than b")

# Nested if else statement
if a > 10:
    print("a is greater than 10")
    if a > 50: 
        print("a is also greater than 50")
    else:
        print("a is not greater than 50")