# List data type 
car_brands = ["Tesla", "Toyota", "BMW", "Lamborghini", "Ford"]
print(car_brands)
print(type(car_brands))

# Tuple data type 
tech_companies = ("Google", "Facebook", "Amazon", "Apple", "Netflix")
print(tech_companies)
print(type(tech_companies))

# Set data type 
animals = {"Lion", "Giraffe", "Dog", "Dolphin", "Cat"}
print(animals)
print(type(animals))