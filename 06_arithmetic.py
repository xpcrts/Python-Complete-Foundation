# Initiate new variables
num1 = 199
num2 = 17.8

# Addition operator
result = num1 + num2
print(result)

# Substraction operator
result = num1 - num2
print(result)

# Multiplication operator 
result = num1 * num2 
print(result)

# Division operator 
result = num1 / num2
print(result)

# Modulo operator 
result = num1 % num2
print(result)